class Task:
    def __init__(self, task_name, task_desc, date, time, task_sub, task_cat, status):
        self.task_name = task_name
        self.task_desc = task_desc
        self.date = date
        self.time = time
        self.task_sub = task_sub
        self.task_cat = task_cat
        self.status = status
